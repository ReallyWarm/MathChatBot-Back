CREATE OR REPLACE FUNCTION "public"."get_user_profile"("p_user_id" "uuid") RETURNS TABLE("user_id" "uuid", "first_name" character varying, "last_name" character varying, "email" character varying, "role_name" character varying, "profile_picture" "text", "created_date" timestamp with time zone, "updated_date" timestamp with time zone, "week_status" "jsonb")
    LANGUAGE "sql" STABLE
    SET "search_path" TO 'public'
    AS $$
  select 
    u.user_id,
    u.first_name,
    u.last_name,
    u.public_email,
    r.role_name,
    u.profile_picture,
    u.created_date,
    u.updated_date,
    case when is_student() then
    (
      select jsonb_agg(
        jsonb_build_object(
          'day', to_char(d.day, 'FMDay'),
          'date', extract(day from d.day)::int,
          'count', coalesce(daily.cnt, 0)
        )
      order by d.day
      )
      from generate_series(
        date_trunc('week', current_date),
        date_trunc('week', current_date) + interval '6 days',
        interval '1 day'
      ) as d(day)
      left join (
        select
          date_trunc('day', sa.complete_date) as day,
          count(*) as cnt
        from student_answer sa
        where sa.user_id = p_user_id
          and sa.is_completed = TRUE
          and sa.complete_date >= date_trunc('week', current_date)
          and sa.complete_date < date_trunc('week', current_date) + interval '7 days'
        group by date_trunc('day', sa.complete_date)
      ) daily on daily.day = d.day
    ) 
    else null
    end as week_status
