CREATE OR REPLACE FUNCTION "public"."edit_question"("p_question_id" bigint, "p_title" "text", "p_question" "text", "p_answer" "text", "p_difficulty" "public"."difficulty", "p_is_published" boolean, "p_new_tags" bigint[], "p_delete_old_hint" boolean DEFAULT false) RETURNS "void"
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public'
    AS $$
declare
  v_user_id uuid := auth.uid();
  v_existing_tags bigint[];
  v_new_tags bigint[];
  v_tags_changed boolean;
begin
  if v_user_id is null then
    raise exception 'Not authenticated';
  end if;
  
  -- check question tag change
  if p_new_tags is null then
    v_tags_changed := false;
  else
    select coalesce(array_agg(distinct t order by t), '{}'::bigint[])
    into v_new_tags
    from unnest(coalesce(p_new_tags, '{}'::bigint[])) as t;

    select coalesce(array_agg(tag_id order by tag_id), '{}'::bigint[])
    into v_existing_tags 
    from question_tag
    where question_id = p_question_id;

    v_tags_changed := v_existing_tags <> v_new_tags;
  end if;
  -- 

  if p_title is null 
    and p_question is null
    and p_answer is null
    and p_difficulty is null
    and p_is_published is null
    and not v_tags_changed
  then
    return;
  end if;

  update question q
  set
    title = coalesce(p_title, q.title),
    question = coalesce(p_question, q.question),
    answer = coalesce(p_answer, q.answer),
    difficulty = coalesce(p_difficulty::"difficulty", q.difficulty),
    is_published = coalesce(p_is_published, q.is_published),
    updated_date = now()
  where q.question_id = p_question_id;

  if not found then
    raise exception 'Question update failed';
  end if;

  -- update question tag
  if v_tags_changed then
    delete from question_tag qt
    where qt.question_id = p_question_id
      and qt.tag_id not in (
        select unnest(v_new_tags::bigint[])
      );

    if coalesce(cardinality(p_new_tags), 0) > 0 then
      insert into question_tag (
        question_id, 
        tag_id
      )
      select p_question_id, unnest(v_new_tags::bigint[])
      on conflict do nothing;
    end if;
  end if;

  if p_delete_old_hint then
    delete from vector.hint h
    where h.question_id = p_question_id;
  end if;
  --
end;
$$;


