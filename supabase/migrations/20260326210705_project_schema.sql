CREATE OR REPLACE FUNCTION "util"."retry_embedding_jobs"("batch_size" integer DEFAULT 5, "timeout_milliseconds" integer DEFAULT ((5 * 60) * 1000)) RETURNS "void"
    LANGUAGE "plpgsql"
    AS $$
declare
  job record;
  auth_header text;
begin
  -- Resolve auth header
  select 'Bearer ' || decrypted_secret
  into auth_header
  from vault.decrypted_secrets
  where name = 'service_role_key';

  for job in
    select *
      from pgmq.read(
        queue_name => 'embedding_jobs',
        vt => timeout_milliseconds / 1000,
        qty => batch_size
      )
  loop
    perform util.invoke_edge_function(
      name => 'embed',
      auth_header => auth_header,
      body => job.message || jsonb_build_object(
        'job_id', job.msg_id,
        'attempt', coalesce((job.message->>'attempt')::int, 1) + 1
      ),
      timeout_milliseconds => timeout_milliseconds
    );
  end loop;
end;
$$;


