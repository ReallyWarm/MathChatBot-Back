CREATE OR REPLACE FUNCTION "util"."handle_file_upsert_embedding"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO ''
    AS $$
declare
  job_id bigint;
  auth_header text;
  course_id bigint;
  path_parts text[];
  file_name text;
  timeout_milliseconds int := 5 * 60 * 1000;
begin
  -- Parse storage file path
  path_parts := string_to_array(NEW.name, '/');
  
  -- Private folder: document_theory
  if (path_parts[1])::text = 'private' then
    course_id := null;
    file_name := (path_parts[2])::text;

  -- Course_id folder: document_course
  else
    course_id := (path_parts[1])::bigint;
    file_name := (path_parts[array_length(path_parts, 1)])::text;
  end if;

  -- Create Document on new upload
  if TG_OP = 'INSERT' then
    insert into vector.document(
      doc_id, 
      course_id, 
      title
    )
    values (
      NEW.id, 
      course_id, 
      file_name
    );

  -- Update existing Document
  elsif TG_OP = 'UPDATE' then
    update vector.document
    set 
      title = file_name,
      course_id = course_id,
      updated_date = now()
    where doc_id = NEW.id;

    -- If no Document exists
    if doc_id is null then
      insert into vector.document(
        doc_id, 
        course_id, 
        title
      )
      values (
        NEW.id, 
        course_id, 
        file_name
      );
    end if;
  end if;

  -- Resolve auth header
  select 'Bearer ' || decrypted_secret
  into auth_header
  from vault.decrypted_secrets
  where name = 'service_role_key';

  -- Enqueue with delay as retry safety net
  select pgmq.send(
    queue_name => 'embedding_jobs',
    msg => jsonb_build_object(
      'doc_id', NEW.id,
      'bucket_id', NEW.bucket_id,
      'name', NEW.name,
      'operation', TG_OP,
      'attempt', 1
    ),
    delay => timeout_milliseconds / 1000
  ) into job_id;

  -- Immediately dispatch to Edge Function
  perform util.invoke_edge_function(
    name => 'embed',
    auth_header => auth_header,
    body => jsonb_build_object(
      'job_id', job_id,
      'doc_id', NEW.id,
      'bucket_id', NEW.bucket_id,
      'name', NEW.name,
      'operation', TG_OP,
      'attempt', 1
    ),
    timeout_milliseconds => timeout_milliseconds
  );

  return NEW;
end;
$$;


ALTER FUNCTION "util"."handle_file_upsert_embedding"() OWNER TO "postgres";


