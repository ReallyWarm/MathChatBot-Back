CREATE OR REPLACE FUNCTION "public"."delete_question"("p_question_id" bigint) RETURNS "jsonb"
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public'
    AS $$
declare
  v_user_id uuid := auth.uid();
  v_course_id bigint;
  v_file_path text[];
begin
  if v_user_id is null then
    raise exception 'Not authenticated';
  end if;

  select t.course_id into v_course_id
  from question q
  join topic t 
    on t.topic_id = q.topic_id
  where q.question_id = p_question_id;

  select array_agg(obj.name) into v_file_path
  from storage.objects obj 
  where bucket_id = 'question_image'
    AND (storage.foldername(obj.name))[1] = v_course_id::text
    AND (storage.foldername(obj.name))[2] = p_question_id::text;

  delete from question q
    where q.question_id = p_question_id;

  if not found then
    raise exception 'Question delete failed';
  end if;
  
  return jsonb_build_object(
    'question_id', p_question_id,
    'question_file_path', v_file_path
  );
end;
$$;


