CREATE OR REPLACE FUNCTION "public"."check_course_completion"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
declare
    v_course_id bigint;
    v_total_questions int;
    v_completed_questions int;
begin
    -- Get course_id
    select t.course_id into v_course_id
    from question q
    join topic t 
      on q.topic_id = t.topic_id
    where q.question_id = new.question_id;

    -- Count total questions
    select count(q.question_id) into v_total_questions
    from question q
    join topic t 
      on q.topic_id = t.topic_id
    where t.course_id = v_course_id
      and q.is_published = true;

    -- Count completed questions
    select count(sa.question_id) into v_completed_questions
    from student_answer sa
    join question q 
      on sa.question_id = q.question_id
    join topic t 
      on q.topic_id = t.topic_id
    where t.course_id = v_course_id
      and sa.user_id = new.user_id
      and sa.is_completed = true
      and q.is_published = true;

    -- Update enroll
    if v_total_questions > 0 and v_completed_questions >= v_total_questions then
      update enroll
      set is_completed = true,
        completed_date = now()
      where user_id = new.user_id
        and course_id = v_course_id;
    end if;

    return new;
end;
$$;


ALTER FUNCTION "public"."check_course_completion"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."check_student_answer"("p_question_id" bigint, "p_student_answer" "text") RETURNS boolean
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public'
    AS $$
declare
  v_user_id uuid := auth.uid();
  v_course_id bigint;
  v_is_correct boolean;
begin
  if v_user_id is null then
    raise exception 'Not authenticated';
  end if;

  select t.course_id into v_course_id
  from question as q
  join topic t
    on q.topic_id = t.topic_id
  where q.question_id = p_question_id;

  if v_course_id is null then
    raise exception 'Course not found';
  end if;

  if not has_enroll(v_course_id) then
    raise exception 'Not enrolled';
  end if;

  if p_student_answer is null or trim(p_student_answer) = '' 
  then
    return false;
  end if;

  insert into student_answer(
    user_id, 
    question_id, 
    answer
  )
  values (
    v_user_id,
    p_question_id,
    p_student_answer
  )
  on conflict (user_id, question_id)
  do update
  set answer = EXCLUDED.answer
  where student_answer.is_completed = false;

  select lower(trim(q.answer::text)) = lower(trim(p_student_answer::text))
  into v_is_correct
  from question q
  where q.question_id = p_question_id;

  if v_is_correct then
  update student_answer sa
  set
    is_completed = true,
    complete_date = now()
  where 
    sa.question_id = p_question_id
    and sa.user_id = v_user_id
    and sa.is_completed = false;
  end if;

  return v_is_correct;
end;
$$;


ALTER FUNCTION "public"."check_student_answer"("p_question_id" bigint, "p_student_answer" "text") OWNER TO "postgres";

