    LANGUAGE "plpgsql" STABLE
    SET "search_path" TO 'public'
    AS $$
declare
  v_user_id uuid := auth.uid();
  v_last_difficulty difficulty := null;
begin
  select c.difficulty into v_last_difficulty
  from enroll e
  join course c
    on e.course_id = c.course_id
  where e.user_id = v_user_id
  order by enroll_date desc
  limit 1;

  return query
  select 
    c.course_id,
    c.course_name, 
    coalesce(
    nullif(concat_ws(' ', u.first_name, u.last_name), ''),
    'Unknown User'
    ) as course_owner,
    c.difficulty,  
    c.course_description,
    c.banner_picture,
    c.is_published,
    coalesce(sc.student_amount, 0) as student_amount
  from course c
  left join public.user u
    on c.created_by_uid = u.user_id
  left join (
    select enroll.course_id, count(distinct user_id) as student_amount
    from enroll
    group by enroll.course_id
  ) sc on c.course_id = sc.course_id
  where c.is_published
  and (v_user_id is null or c.created_by_uid <> v_user_id)
  order by 
    case
      when v_last_difficulty is null then 0
      else abs(
        array_position(enum_range(null::difficulty), c.difficulty)
        -
        array_position(enum_range(null::difficulty), v_last_difficulty)
      )
    end,
    coalesce(sc.student_amount, 0) desc, 
    c.updated_date desc
  limit 5;
end;
