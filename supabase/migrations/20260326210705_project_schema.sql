CREATE OR REPLACE FUNCTION "public"."search_course"("p_query" "text", "p_difficulty" "public"."difficulty", "p_current_page" integer, "p_page_size" integer) RETURNS "jsonb"
    LANGUAGE "plpgsql" STABLE
    SET "search_path" TO 'public', 'extensions'
    AS $$
declare
  v_offset int := (p_current_page - 1) * p_page_size;
  v_total bigint;
begin
  select count(*)
  into v_total
  from course c
  where (c.is_published or c.created_by_uid = auth.uid())
    and (p_query is null or p_query = '' 
      or c.course_name % p_query or c.course_name ilike '%' || p_query || '%')
    and (p_difficulty is null or c.difficulty = p_difficulty);

  return jsonb_build_object(
    'page', p_current_page,
    'page_size', p_page_size,
    'total_items', v_total,
    'total_pages', (v_total + p_page_size - 1) / p_page_size,
    'items',
    (
      select coalesce(jsonb_agg(row_to_json(t)), '[]'::jsonb)
      from (
        select 
          c.course_id,
          c.course_name,
          coalesce(
            nullif(concat_ws(' ', u.first_name, u.last_name), ''),
            'Unknown User'
          ) as course_owner,
          c.difficulty,
          c.course_description,
          c.banner_picture,
          c.is_published,
          coalesce(sc.student_amount, 0) as student_amount
        from course c
        left join public.user u
          on c.created_by_uid = u.user_id
        left join (
          select course_id, count(distinct user_id) as student_amount
          from enroll
          group by course_id
        ) sc on sc.course_id = c.course_id
        where (c.is_published or c.created_by_uid = auth.uid())
          and (p_query is null or p_query = '' 
            or c.course_name % p_query or c.course_name ilike '%' || p_query || '%')
          and (p_difficulty is null or c.difficulty = p_difficulty)
        order by
          case
            when p_query is null or p_query = '' then c.course_id
            else similarity(c.course_name, p_query)
          end desc
        limit p_page_size
        offset v_offset
      ) t
    )
  );
end;
$$;


