CREATE OR REPLACE FUNCTION "public"."get_course_completion_status"("p_user_id" "uuid", "p_max_size" integer) RETURNS TABLE("course_id" bigint, "course_name" character varying, "is_completed" boolean, "completion_percentage" double precision, "last_active_date" timestamp with time zone)
    LANGUAGE "plpgsql" STABLE
    SET "search_path" TO 'public'
    AS $$
