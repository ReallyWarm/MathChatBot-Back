  return query
  select
    c.course_id,
    c.course_name,
    e.is_completed,
    (
      count(sa.question_id) filter (where sa.is_completed = true)::float
      / nullif(count(distinct q.question_id), 0)
    ) * 100 as completion_percentage,
    greatest(
      max(sa.start_date),
      max(sa.complete_date)
    ) as last_active_date
  from enroll e
  left join course c
    on c.course_id = e.course_id
  left join topic t
    on t.course_id = c.course_id
  left join question q
    on q.topic_id = t.topic_id
    and q.is_published = true
  left join student_answer sa
    on sa.question_id = q.question_id
    and sa.user_id = e.user_id
  where e.user_id = p_user_id
    and c.is_published = true
  group by c.course_id, e.is_completed
  order by last_active_date desc nulls last
  limit p_max_size;
end;
