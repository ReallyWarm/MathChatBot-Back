CREATE OR REPLACE FUNCTION "public"."recheck_course_completion_on_question_unpublished"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
declare
    v_course_id bigint;
    v_total_questions int;
begin
    -- Get course_id
    select t.course_id into v_course_id
    from topic t 
    where t.topic_id = old.topic_id;

    -- Count total questions
    select count(q.question_id) into v_total_questions
    from question q
    join topic t 
      on q.topic_id = t.topic_id
    where t.course_id = v_course_id
      and q.is_published = true;

    -- Recheck enroll
    update enroll e
    set 
      is_completed = true,
      completed_date = now()
    where e.course_id = v_course_id
      and e.is_completed = false
      and v_total_questions > 0
      and (
        select count(sa.question_id)
        from student_answer sa
        join question q 
          on sa.question_id = q.question_id
        join topic t 
          on q.topic_id = t.topic_id
        where t.course_id = v_course_id
          and sa.user_id = e.user_id
          and sa.is_completed = true
          and q.is_published = true
      ) >= v_total_questions;

    return old;
end;
$$;


